"""Load dataset"""

dataset = pd.read_csv('dataset.csv')

"""Summarize data"""

print(dataset.shape)
print(dataset.head(5))

"""Visualize dataset"""

plt.xlabel('Area')
plt.ylabel('Price')
plt.scatter(dataset.area, dataset.price, color='maroon', marker = '*')

"""Segregate into x(input) and y(output)"""

x = dataset.drop('price',axis='columns')
print(x)
y = dataset.price
print(y)

"""Training dataset using Linear regression"""

model = LinearRegression()
model.fit(x,y)

"""Prediction"""

x=6660
LandAreainSqft = [[x]]
predictedResult = model.predict(LandAreainSqft)
print(predictedResult)

"""Check our model ( Theory calculation : y = mx +b)"""

m = model.coef_ 
m

b = model.intercept_
b

y = m*x+b
print("Price of the {0} square feet land is : {1} ".format(x,y[0]))

